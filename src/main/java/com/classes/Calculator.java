package com.classes;

public class Calculator {
    public double sum(Double a, Double b) {
        return a + b;
    }

    public double mul(Double a, Double b) {
        return a * b;
    }

    public double divide(Double a, double b) {
        return a / b;
    }

    public double diff(Double a, Double b) {
        return a - b;
    }
}

